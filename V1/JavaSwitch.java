public class JavaSwitch {
    
    public static void main(String[] args) {
        


        String name = "tanzil" ;


        switch (name) {
            case "shakil":
                System.out.println("Shakil Good to go");
                break;

            case "tanzil":
                System.out.println("Tanzil Good to go");
                break;    
        
            default:

                System.out.println("Nobody is selected");    
                break;
        }

    }
}
